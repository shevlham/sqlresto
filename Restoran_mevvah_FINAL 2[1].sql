
CREATE DATABASE IF NOT EXISTS RestoranMevvah;
USE RestoranMevvah;

-- Tabel Supplier //
CREATE TABLE Supplier (
    ID_Supplier INT PRIMARY KEY,
    Nama_Toko VARCHAR(100),
    No_Telepon VARCHAR(20),
    Tanggal DATE,
    Waktu TIME,
    Nama_Barang VARCHAR(100),
    Jumlah_Barang INT,
    Total_Pembelian DECIMAL(12,2)
);

-- Tabel Bahan Baku // 
CREATE TABLE BahanBaku (
    ID_BahanBaku INT PRIMARY KEY,
    ID_Supplier INT,
    Nama_Bahan VARCHAR(100),
    Kategori VARCHAR(50),
    Stok DECIMAL(10,2),
    Satuan VARCHAR(20),
    FOREIGN KEY (ID_Supplier) REFERENCES Supplier(ID_Supplier)
);

-- Tabel Menu //
CREATE TABLE Menu (
    ID_Menu INT PRIMARY KEY,
    ID_BahanBaku INT,
    Nama_Menu VARCHAR(100),
    Kategori VARCHAR(50),
    Ketersediaan BOOLEAN,
    Harga DECIMAL(10,2),
    FOREIGN KEY (ID_BahanBaku) REFERENCES BahanBaku(ID_BahanBaku)
);

-- Detail Menu //
CREATE TABLE DetailMenu (
    ID_Menu INT,
    ID_BahanBaku INT,
    Jumlah_Bahan DECIMAL(10,2),
    Satuan VARCHAR(20),
    PRIMARY KEY (ID_Menu, ID_BahanBaku),
    FOREIGN KEY (ID_Menu) REFERENCES Menu(ID_Menu),
    FOREIGN KEY (ID_BahanBaku) REFERENCES BahanBaku(ID_BahanBaku)
);

-- Data Pelanggan //
CREATE TABLE DataPelanggan (
    ID_Pelanggan INT PRIMARY KEY,
    Nama_Lengkap VARCHAR(100),
    No_Telepon VARCHAR(20),
    Email VARCHAR(100),
    Status_Pelanggan ENUM('Pelanggan', 'Tidak') NOT NULL
);

-- Data Pegawai // 
CREATE TABLE DataPegawai (
    ID_Pegawai INT PRIMARY KEY,
    Nama_Pegawai VARCHAR(100),
    No_Telepon VARCHAR(20),
    Email VARCHAR(100),
    Alamat TEXT,
    Tempat_Tanggal_Lahir VARCHAR(100),
    Jenis_Kelamin ENUM('Laki=Laki','Perempuan')NOT NULL,
    Jabatan VARCHAR(50),
    ID_Manager INT,
    FOREIGN KEY (ID_Manager) REFERENCES DataPegawai(ID_Pegawai)
);

-- Meja // 
CREATE TABLE Meja (
    ID_Meja INT PRIMARY KEY,
    Kapasitas INT,
    Jumlah_Meja INT
);

-- Reservasi //
CREATE TABLE Reservasi (
    ID_Reservasi INT PRIMARY KEY,
    ID_Pelanggan INT,
    Tanggal_Reservasi DATE,
    Waktu_Reservasi TIME,
    Status_DP ENUM('Lunas','Belum')NOT NULL,
    FOREIGN KEY (ID_Pelanggan) REFERENCES DataPelanggan(ID_Pelanggan)
);

-- Detail Reservasi //
CREATE TABLE DetailReservasi (
    ID_Reservasi INT,
    ID_Meja INT,
    Status ENUM('Reservasi', 'Tidak')NOT NULL,
    Catatan TEXT,
    PRIMARY KEY (ID_Reservasi, ID_Meja),
    FOREIGN KEY (ID_Reservasi) REFERENCES Reservasi(ID_Reservasi),
    FOREIGN KEY (ID_Meja) REFERENCES Meja(ID_Meja)
);

-- Pesanan //
CREATE TABLE Pesanan (
    ID_Pesanan INT PRIMARY KEY,
    ID_Pegawai INT,
    ID_Reservasi INT,
    ID_Pelanggan INT,
    Tanggal DATE,
    Waktu TIME,
    Status_Pesanan ENUM('Dine In', 'Take Away')NOT NULL,
    Total_Harga DECIMAL(12,2),
    FOREIGN KEY (ID_Pegawai) REFERENCES DataPegawai(ID_Pegawai),
    FOREIGN KEY (ID_Reservasi) REFERENCES Reservasi(ID_Reservasi),
    FOREIGN KEY (ID_Pelanggan) REFERENCES DataPelanggan(ID_Pelanggan)
);

-- Detail Pesanan  //
CREATE TABLE DetailPesanan (
    ID_Pesanan INT,
    ID_Menu INT,
    Jumlah_Pesanan INT,
    Harga_Per_Item DECIMAL(10,2),
    Jenis_Pesanan ENUM('Dine in','Take Away')NOT NULL,
    Subtotal DECIMAL(12,2),
    PRIMARY KEY (ID_Pesanan, ID_Menu),
    FOREIGN KEY (ID_Pesanan) REFERENCES Pesanan(ID_Pesanan),
    FOREIGN KEY (ID_Menu) REFERENCES Menu(ID_Menu)
);

-- Dine In Detail //
CREATE TABLE DineInDetail (
    ID_DineIn INT PRIMARY KEY,
    ID_Meja INT,
    ID_Pesanan INT,
    Status_Meja VARCHAR(50),
    FOREIGN KEY (ID_Meja) REFERENCES Meja(ID_Meja),
    FOREIGN KEY (ID_Pesanan) REFERENCES Pesanan(ID_Pesanan)
);

-- Diskon // 
CREATE TABLE Diskon (
    ID_Diskon INT PRIMARY KEY,
    Nama_Diskon VARCHAR(100),
    Jenis_Diskon VARCHAR(50),
    Persentase_Diskon DECIMAL(5,2),
    Jumlah_Diskon DECIMAL(10,2)
);

-- Transaksi //
CREATE TABLE Transaksi (
    ID_Pembayaran INT PRIMARY KEY,
    ID_Pesanan INT,
    ID_Pegawai INT,
    ID_Diskon INT,
    Metode_Pembayaran VARCHAR(50),
    Status_Pembayaran ENUM('Lunas','Belum')NOT NULL,
    Total_Pembayaran DECIMAL(12,2),
    FOREIGN KEY (ID_Pesanan) REFERENCES Pesanan(ID_Pesanan),
    FOREIGN KEY (ID_Pegawai) REFERENCES DataPegawai(ID_Pegawai),
    FOREIGN KEY (ID_Diskon) REFERENCES Diskon(ID_Diskon)
);

-- Pembelian //
CREATE TABLE Pembelian (
    ID_Pembelian INT PRIMARY KEY,
    ID_Supplier INT,
    Tanggal DATE,
    Waktu TIME,
    Total_Pembelian DECIMAL(12,2),
    FOREIGN KEY (ID_Supplier) REFERENCES Supplier(ID_Supplier)
);

-- Detail Pembelian //
CREATE TABLE DetailPembelian (
    ID_Pembelian INT,
    ID_BahanBaku INT,
    Jumlah_Pesanan INT,
    Harga_Per_Item DECIMAL(10,2),
    Jenis_Pesanan VARCHAR(50),
    Subtotal DECIMAL(12,2),
    PRIMARY KEY (ID_Pembelian, ID_BahanBaku),
    FOREIGN KEY (ID_Pembelian) REFERENCES Pembelian(ID_Pembelian),
    FOREIGN KEY (ID_BahanBaku) REFERENCES BahanBaku(ID_BahanBaku)
);

-- Laporan Keuangan //
CREATE TABLE LaporanKeuangan (
    ID_Laporan INT PRIMARY KEY,
    ID_Pegawai INT,
    ID_Supplier INT,
    ID_Pembayaran INT,
    ID_Pembelian INT,
    Tanggal_Pembuatan DATE,
    Jumlah_Pesanan INT,
    Pemasukan DECIMAL(12,2),
    Pengeluaran DECIMAL(12,2),
    FOREIGN KEY (ID_Pegawai) REFERENCES DataPegawai(ID_Pegawai),
    FOREIGN KEY (ID_Supplier) REFERENCES Supplier(ID_Supplier),
    FOREIGN KEY (ID_Pembayaran) REFERENCES Transaksi(ID_Pembayaran),
    FOREIGN KEY (ID_Pembelian) REFERENCES Pembelian(ID_Pembelian)
);

